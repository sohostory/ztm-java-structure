public class AdvancedCalculator {
    public static void performCalculations (int a, int b) {
        int sum = a + b;
        int diff = a - b;
        int prod = a * b;
        double div = a / b;

        System.out.println(sum);
        System.out.println(diff);
        System.out.println(prod);
        System.out.println(div);

    }
}
